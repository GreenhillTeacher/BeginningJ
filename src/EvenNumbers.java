public class EvenNumbers {
    public static void main(String[] args){
        byte num1 = 11;
        short num2 = 46;
        int num3 = 35;
        long num4 = 78;
        // this are the questions to find out if the numbers are even
        if (num1%2 == 0)        // % also known as mod find the reminder
        {
            System.out.println("Num 1 is an even number ");
        }
        if (num2%2 == 0)
        {
            System.out.println("Num 2 is an even number ");
        }
        if (num3%2 == 0)
        {
            System.out.println("Num 3 is an even number ");
        }
        if (num4%2 == 0)
        {
            System.out.println("Num 4 is an even number ");
        }
    }

}
